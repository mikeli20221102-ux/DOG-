// Canonical base URL. Override for production by setting a
// <meta name="site-url" content="https://your-domain.com/"> in <head>.
// Otherwise we use the current deployment origin + path automatically.
function baseUrl() {
  const meta = document.querySelector('meta[name="site-url"]');
  if (meta && meta.content) return meta.content.replace(/\?.*$|#.*$/, "");
  return location.origin + location.pathname;
}

function langUrl(lang) {
  const base = baseUrl();
  return lang === "en" ? base : `${base}?lang=${lang}`;
}

function pickInitialLang() {
  const fromUrl = new URLSearchParams(location.search).get("lang");
  if (fromUrl && I18N[fromUrl]) return fromUrl;
  const saved = localStorage.getItem(I18N_STORE_KEY);
  if (saved && I18N[saved]) return saved;
  const nav = (navigator.language || "en").slice(0, 2).toLowerCase();
  return I18N[nav] ? nav : "en";
}

function t(key) {
  const dict = I18N[currentLang] || I18N.en;
  return dict[key] != null ? dict[key] : (I18N.en[key] != null ? I18N.en[key] : key);
}

// Inject <link rel="alternate" hreflang="..."> tags once, for SEO.
function buildHreflang() {
  document.querySelectorAll("link[data-hreflang]").forEach((n) => n.remove());
  const head = document.head;
  const add = (hreflang, href) => {
    const link = document.createElement("link");
    link.rel = "alternate";
    link.hreflang = hreflang;
    link.href = href;
    link.setAttribute("data-hreflang", "1");
    head.appendChild(link);
  };
  LANGS.forEach((l) => add(l.code, langUrl(l.code)));
  add("x-default", baseUrl());
}

function setMeta(id, value) {
  const el = document.getElementById(id);
  if (el) el.setAttribute("content", value);
}

function applyI18n(lang) {
  if (!I18N[lang]) lang = "en";
  currentLang = lang;
  localStorage.setItem(I18N_STORE_KEY, lang);

  const isRtl = RTL_LANGS.includes(lang);
  document.documentElement.lang = lang;
  document.documentElement.dir = isRtl ? "rtl" : "ltr";

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    el.textContent = t(el.getAttribute("data-i18n"));
  });
  document.querySelectorAll("[data-i18n-ph]").forEach((el) => {
    el.setAttribute("placeholder", t(el.getAttribute("data-i18n-ph")));
  });

  // SEO / social meta
  const title = t("meta.title");
  const desc = t("meta.description");
  document.title = title;
  setMeta("metaDesc", desc);
  setMeta("ogTitle", title);
  setMeta("ogDesc", desc);
  setMeta("twTitle", title);
  setMeta("twDesc", desc);
  setMeta("ogLocale", OG_LOCALE[lang] || "en_US");
  setMeta("ogUrl", langUrl(lang));
  const canonical = document.getElementById("canonicalLink");
  if (canonical) canonical.href = langUrl(lang);

  // Reflect the active language in the URL (good for sharing & crawling).
  const url = new URL(location.href);
  if (lang === "en") url.searchParams.delete("lang");
  else url.searchParams.set("lang", lang);
  history.replaceState(null, "", url);

  const sel = document.getElementById("langSelect");
  if (sel && sel.value !== lang) sel.value = lang;

  document.dispatchEvent(new CustomEvent("langchange", { detail: { lang } }));
}

function initI18n() {
  buildHreflang();
  const sel = document.getElementById("langSelect");
  if (sel) {
    sel.innerHTML = LANGS.map((l) => `<option value="${l.code}">${l.name}</option>`).join("");
    sel.addEventListener("change", () => applyI18n(sel.value));
  }
  applyI18n(pickInitialLang());
}

